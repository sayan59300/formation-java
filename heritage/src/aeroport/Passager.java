package aeroport;

/**
 *
 * @author sayan593
 */
public class Passager {

    private final int id;
    private final String nom;
    private final String prenom;

    public Passager(int id, String nom, String prenom) {
        this.id = id;
        this.nom = nom;
        this.prenom = prenom;
    }

    public int getId() {
        return id;
    }

    public String getNom() {
        return nom;
    }

    public String getPrenom() {
        return prenom;
    }

}
